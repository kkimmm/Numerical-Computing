%---------自己编写的三次样条插值函数主调函数-----------
clc;clear;
X=[0 1 2 3 ];
Y=[0 0.5 2.0 1.5 ];
dy1=0.2;%边界值条件一
dyn=-1;%边界值条件二
x=0:0.1:3;  %这是需要插值计算的点集
y=Spline(X,Y,dy1,dyn,x)
plot(X,Y,'or',x,y,'b')
legend('样本点','三次样条插值结果')